#[cfg(test)]
mod tests {
    use sha256::hash;

    #[test]
    fn hash_test() {
        let input = "Hello";
        let expected_hash: [u8; 32] = [
            0x18, 0x5F, 0x8D, 0xB3, 0x22, 0x71, 0xFE, 0x25, 0xF5, 0x61, 0xA6, 0xFC, 0x93, 0x8B,
            0x2E, 0x26, 0x43, 0x06, 0xEC, 0x30, 0x4E, 0xDA, 0x51, 0x80, 0x07, 0xD1, 0x76, 0x48,
            0x26, 0x38, 0x19, 0x69,
        ];
        assert_eq!(hash(input), expected_hash);

        let input_long = "Hello, this is a long string\n".repeat(1000);
        let expected_hash_long: [u8; 32] = [
            0x86, 0x8E, 0x29, 0xBB, 0x3F, 0x2B, 0xCE, 0xA5, 0x91, 0x6D, 0xA9, 0x03, 0x5A, 0x64,
            0x5E, 0x28, 0x84, 0x71, 0x75, 0x3F, 0x0C, 0xF7, 0x7C, 0xA9, 0xD9, 0xED, 0x05, 0x5A,
            0x42, 0x5F, 0xC5, 0x1D,
        ];
        assert_eq!(hash(&input_long), expected_hash_long);
    }
}
